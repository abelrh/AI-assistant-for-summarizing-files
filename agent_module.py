# agent_module.py
import os
import streamlit as st
from dotenv import load_dotenv
from langchain_core.prompts import ChatPromptTemplate
from langchain_huggingface import ChatHuggingFace, HuggingFaceEndpoint

load_dotenv()


def generate_full_summary(full_text: str):
    """
    إرسال النص الكامل للمستند إلى Hugging Face Inference API باستخدام نموذج Qwen2.5 المتفوق
    في اللغة العربية والتحليل المطول، وصياغة قراءة استقصائية إذاعية فخمة.
    """
    # جلب التوكن الخاص بـ Hugging Face بشكل مرن (أونلاين أو محلياً)
    hf_token = st.secrets.get("HF_TOKEN") or os.getenv("HF_TOKEN")

    if not hf_token:
        raise ValueError(
            "لم يتم العثور على HF_TOKEN. أضفه في ملف .env أو إعدادات Secrets بالشكل: HF_TOKEN=hf_..."
        )

    # اختيار نموذج Qwen 2.5 (الأقوى عالمياً حالياً في فهم وتوليد اللغة العربية بدقة مذهلة وسياق ضخم)
    # يمكنك أيضاً التبديل إلى "meta-llama/Llama-3.3-70B-Instruct" إذا كنت تفضل لاما
    repo_id = "Qwen/Qwen2.5-72B-Instruct"

    # تهيئة الـ Endpoint من Hugging Face
    llm_endpoint = HuggingFaceEndpoint(
        repo_id=repo_id,
        max_new_tokens=3500,  # مساحة مخرجات تفصيلية ضخمة للتلخيص المطول
        temperature=0.5,
        huggingfacehub_api_token=hf_token,
    )

    # دمج الـ Endpoint داخل هيكل ChatHuggingFace ليتوافق مع نظام الـ Prompts والشات
    llm = ChatHuggingFace(llm=llm_endpoint)

    # صياغة System Prompt احترافي يركز على العمق الأكاديمي والتحليل الشامل والربط الإذاعي السلس
    system_prompt = """أنت كبير المستشارين ومذيع ومحلل تقني فذ على درجة عالية من الخبرة في تفكيك المستندات والأوراق العلمية المعقدة باللغة العربية.
مهمتك الحالية هي تقديم "قراءة استقصائية تفصيلية ممتدة" للمستند المرفق بالكامل، بأسلوب إذاعي بشري فخم وتفاعلي، بعيداً تماماً عن الاختصار السطحي المخل.

تعليمات صارمة للتحليل والصياغة الفائقة (لتتفوق على أي نموذج آخر):
1. **المقدمة الإذاعية الفخمة**: ابدأ بترحيب مميز وإثارة للاهتمام حول المستند وطبيعته ومدى تأثيره (مثال: أهلاً بك عزيزي القارئ في هذه الجلسة التحليلية الخاصة.. اليوم نغوص سوياً في أعماق وثيقة في غاية الأهمية.. إلخ).
2. **التحليل البنيوي والاستفاضة**: لا تترك فكرة فرعية، أو آلية عمل، أو نتيجة إلا وتقوم بذكرها وشرحها وتفكيكها هندسياً وثنائياً. خذ وقتك ومساحتك الكاملة في الشرح والتحليل النصي الممتد لكل قسم من أقسام المستند من بدايته وحتى نهايته.
3. **الربط السلس والتفاعل البشري**: استخدم عبارات انتقالية ذكية وراقية تزيد من تشويق المستمع وتجعل النص يتدفق كأنه برنامج إذاعي مباشر (مثل: "وإذا أردنا تفكيك هذا المفهوم بشكل أعمق نجد أن.."، "والآن دعني آخذك إلى الجانب الأكثر عبقرية في هذه البنية وهو.."، "هذه النتيجة تقودنا مباشرة إلى التساؤل الأهم..").
4. **جمالية ونقاء النطق الصوتي**: اكتب بلغة عربية فصحى بالغة الطلاقة والوضوح وقريبة من القلب. تجنب تماماً استخدام التعدادات الجافة (مثل 1، 2، 3) أو الرموز وعلامات الماركداون الحادة؛ ادمج كل الأفكار في فقرات نصية سردية غنية وانسيابية ليكون النطق من خلال الـ TTS بشرياً وتفاعلياً ومريحاً للأذن بنسبة مئة بالمئة.
5. **الخاتمة والأثر**: اختم بقراءة نقدية سريعة للأثر الذي يتركه هذا المستند، تليها خاتمة مقتضبة وشكر وتمنيات بيوم سعيد."""

    prompt = ChatPromptTemplate.from_messages(
        [
            ("system", system_prompt),
            (
                "human",
                "إليك النص الكامل للمستند المرفوع:\n\n{document_text}\n\nقم بصياغة القراءة التحليلية الاستقصائية والمطولة بالكامل الآن بناءً على التعليمات ولا تختصر نهائياً.",
            ),
        ]
    )

    chain = prompt | llm
    result = chain.invoke({"document_text": full_text})
    return result.content
